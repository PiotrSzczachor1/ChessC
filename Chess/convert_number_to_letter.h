char convert_number_to_letter( int c)
{
    char kolumna;
    if(c==0)
    {
        kolumna='A';
    }
    if(c==1)
    {
        kolumna='B';
    }
    if(c==2)
    {
        kolumna='C';
    }
    if(c==3)
    {
        kolumna='D';
    }
    if(c==4)
    {
        kolumna='E';
    }
    if(c==5)
    {
        kolumna='F';
    }
    if(c==6)
    {
        kolumna='G';
    }
    if(c==7)
    {
        kolumna='H';
    }
    return kolumna;
}
